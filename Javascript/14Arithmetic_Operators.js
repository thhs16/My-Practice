

// Arithmetic Operator --->   
//  +  
//  - 
//  *  
//  /  
//  **  
//  %     (remainder after division)    
//  1++   (increment)   
//  1--   (decrement)

let add = 1+2;
let sub = 5-2;
let mul = 1*3;
let div = 6/2;
let exp = 3**1;
let rem = 11%4;

console.log(add, sub, mul, div, exp, rem);

let a=0;

console.log(a++);//Show "a" value first, after that, add "1" to "a" 
console.log(a);
console.log(++a);//Add "1" to "a" right away, just after that, show "a" value

let b=3;
console.log("*****For 'b'*****")
console.log(b--);
